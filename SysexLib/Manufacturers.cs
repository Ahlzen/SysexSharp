    /// <summary>
    /// Returns the manufacturer ID bytes for the
    /// given sysex.
    /// </summary>
    /// <exception cref="ArgumentException">
    /// Thrown if the specified data is not a valid sysex.
    /// </exception>
    public static byte[] GetId(byte[] data)
    {
        // Sanity checks
        if (data == null)
            throw new ArgumentNullException(nameof(data));
        if (data.Length <= 3)
            throw new ArgumentException("Data is too short for a valid Sysex");

        // Manufacturer ID is 1 or 3 bytes following START_OF_SYSEX
        if (data[1] == 0x00)
            return new[] { data[1], data[2], data[3] };
        else
            return new[] { data[1] };
    }

    /// <summary>
    /// Returns the manufacturer name for the given manufacturer
    /// ID, or null if not known.
    /// </summary>
    public static string? GetName(byte[] id)
    {
        // Sanity checks
        if (id == null)
            throw new ArgumentNullException(nameof(id));
        if (!(id.Length == 1 || id.Length == 3))
            throw new ArgumentException("ID is not the correct number of bytes");

        // Look up in index
        return _manufacturers.GetValueOrDefault(id);
    }

